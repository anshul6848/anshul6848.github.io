function setLanguage(lang) {
    localStorage.setItem('divine_lang', lang);
    document.documentElement.lang = lang;
    
    // Update simple text elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    // Update toggle button text
    const toggleBtn = document.getElementById('lang-toggle');
    if(toggleBtn) {
        toggleBtn.innerText = lang === 'en' ? 'हिंदी' : 'English';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('divine_lang') || 'en';
    
    // Create toggle button if it doesn't exist
    if (!document.getElementById('lang-toggle')) {
        const nav = document.querySelector('nav');
        if (nav) {
            const btn = document.createElement('button');
            btn.id = 'lang-toggle';
            btn.className = 'lang-btn';
            btn.onclick = () => {
                const current = localStorage.getItem('divine_lang') || 'en';
                const next = current === 'en' ? 'hi' : 'en';
                setLanguage(next);
            };
            nav.appendChild(btn);
        }
    }

    setLanguage(savedLang);
});

